#include <iostream>
using namespace std;

int main()
{
    int a ;
    int b ;
    int k;
    cin >> k;
    for (int i = 0; i < k; i++)
    {
        cin >> a >> b;
        cout << a + b << endl;
    }
    return 0;
}
/*На первой строке идет целое число k — количество тестов, далее следует k строк, в каждой из которых через пробел идут два целых числа a и b. На выводе для каждой из строк сумма чисел*/
